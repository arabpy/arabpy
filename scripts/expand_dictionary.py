def is_public_name(name: str) -> bool:
    """Check if a name is public (not starting with underscore)."""
    return not name.startswith('_')

def get_module_symbols(module_name: str) -> Dict[str, str]:
    """Extract all public symbols from a module."""
    try:
        module = importlib.import_module(module_name)
    except Exception as e:
        print(f"Skipping {module_name}: {e}")
        return {}
    
    symbols = {}
    
    # Get all public attributes
    for name in dir(module):
        if is_public_name(name):
            obj = getattr(module, name)
            
            # Determine the type of symbol
            symbol_type = None
            if inspect.isclass(obj):
                symbol_type = 'class'
            elif inspect.isfunction(obj):
                symbol_type = 'function'
            elif inspect.ismethod(obj):
                symbol_type = 'method'
            elif inspect.iscoroutine(obj):
                symbol_type = 'coroutine'
            elif inspect.isgeneratorfunction(obj):
                symbol_type = 'generator'
            elif inspect.isasyncgenfunction(obj):
                symbol_type = 'async_generator'
            elif isinstance(obj, (int, float, str, bool, list, tuple, dict, set, frozenset, bytes, bytearray)):
                symbol_type = 'builtin_type'
            elif callable(obj):
                symbol_type = 'callable'
            else:
                # Check if it's an exception
                if inspect.isclass(obj) and issubclass(obj, Exception):
                    symbol_type = 'exception'
                else:
                    symbol_type = 'other'
            
            symbols[name] = symbol_type
    
    return symbols

